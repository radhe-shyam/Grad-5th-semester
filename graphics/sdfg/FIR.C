#include<stdio.h>
#include<conio.h>
#include<graphics.h>
#include<dos.h>
void main()
{
	int gd=9,gm=1,mx,my;
	float xs,ys,xn=0.5,yn=0.5;
	clrscr();
	initgraph(&gd,&gm,"C:\\tc\\bgi");
	mx=getmaxx();
	my=getmaxy();
	xs=xn*mx+0;
	ys=yn*my+0;

	circle(xs,ys,50);
	getch();
	closegraph();
}